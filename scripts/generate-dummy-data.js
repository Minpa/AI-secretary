async function sendRequest(url, data) {
    try {
        const response = await axios.post(url, data, {
            headers: { 'Content-Type': 'application/json' }
        });
        return response.data;
    } catch (error) {
        console.error(`Error sending request to ${url}:`, error.message);
        return null;
    }
}

